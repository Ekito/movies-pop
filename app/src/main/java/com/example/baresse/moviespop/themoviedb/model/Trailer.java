package com.example.baresse.moviespop.themoviedb.model;

public class Trailer {

    private final static String YOUTUBE_BASE_URL = "https://www.youtube.com/watch?v=";
    private final static String YOUTUBE_THUMBNAIL_BASE_URL = "https://i.ytimg.com/vi/";
    private final static String YOUTUBE_THUMBNAIL_SUFFIX = "/hqdefault.jpg";


    private String id;
    private String iso_639_1;
    private String iso_3166_1;
    private String key;
    private String name;
    private String site;
    private int size;
    private String type;

    public String getUrl() {
        if ("youtube".equalsIgnoreCase(site)) {
            return YOUTUBE_BASE_URL + key;
        } else {
            return "";
        }
    }

    public String getThumbnailUrl() {
        if ("youtube".equalsIgnoreCase(site)) {
            return YOUTUBE_THUMBNAIL_BASE_URL + key + YOUTUBE_THUMBNAIL_SUFFIX;
        } else {
            return "";
        }
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getIso_639_1() {
        return iso_639_1;
    }

    public void setIso_639_1(String iso_639_1) {
        this.iso_639_1 = iso_639_1;
    }

    public String getIso_3166_1() {
        return iso_3166_1;
    }

    public void setIso_3166_1(String iso_3166_1) {
        this.iso_3166_1 = iso_3166_1;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSite() {
        return site;
    }

    public void setSite(String site) {
        this.site = site;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public String toString() {
        return "Trailer{" +
                "id='" + id + '\'' +
                ", iso_639_1='" + iso_639_1 + '\'' +
                ", iso_3166_1='" + iso_3166_1 + '\'' +
                ", key='" + key + '\'' +
                ", name='" + name + '\'' +
                ", site='" + site + '\'' +
                ", size=" + size +
                ", type='" + type + '\'' +
                '}';
    }
}
